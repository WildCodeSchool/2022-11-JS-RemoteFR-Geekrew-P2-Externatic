const models = require("../models");
const validateConsultant = require("../validator/consultant.validator");

const browse = (req, res) => {
  models.consultant
    .findAll()
    .then(([rows]) => {
      res.send(rows);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const read = (req, res) => {
  models.consultant
    .find(req.params.id)
    .then(([rows]) => {
      if (rows[0] == null) {
        res.sendStatus(404);
      } else {
        res.send(rows[0]);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const edit = (req, res) => {
  const consultant = req.body;

  const validationResult = validateConsultant(consultant);

  if (validationResult) {
    return res.status(400).send(validationResult);
  }

  consultant.id = parseInt(req.params.id, 10);

  return models.consultant
    .update(consultant)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const add = (req, res) => {
  const consultant = req.body;

  const validationResult = validateConsultant(consultant);

  if (validationResult) {
    return res.status(400).send(validationResult);
  }

  return models.consultant
    .insert(consultant)
    .then(([result]) => {
      res.location(`/consultants/${result.insertId}`).sendStatus(201);
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

const destroy = (req, res) => {
  models.consultant
    .delete(req.params.id)
    .then(([result]) => {
      if (result.affectedRows === 0) {
        res.sendStatus(404);
      } else {
        res.sendStatus(204);
      }
    })
    .catch((err) => {
      console.error(err);
      res.sendStatus(500);
    });
};

module.exports = {
  browse,
  read,
  edit,
  add,
  destroy,
};
